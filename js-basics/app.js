"use strict";

/**
 * Arithmetic operators
 */

// Addition
console.log(2 + 3);

// Subtraction
console.log(40 - 3);

// Multiplication
console.log(3 * 3);

// Division
console.log(20 / 2);

// Power
console.log(2 ** 4);

// Remainder
console.log(3 % 2);

// Unary operators

// Unary minus
console.log(-"40");

// Unary plus
console.log(typeof +"123");

// Assignment
let count = 1;

count = count + 3;
console.log(count);

count += 3;

// Increment
let x = 1;
console.log(++x);
console.log(x);

console.log(x++);
console.log(x);

// Decrement
let y = 1;
console.log(--y);
console.log(y);

console.log(y--);
console.log(y);

// Comparison operators
console.log(5 > 3);
console.log(5 < 10);
console.log(5 == 5);
console.log(5 === "5");
console.log(5 != 3);
console.log(5 !== "3");

console.log("abc" === "abd");
