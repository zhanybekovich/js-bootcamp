"use strict";

/**
 * Variables and use strict
 */

// Declare variable with value
let message = "Hello!";
console.log(message);

// Change value of the variable
message = "Bye!";
console.log(message);

// Old way of declaring variable
var message2 = "Hi!";
console.log(message2);

// Constant variables
const message3 = "Good Day!";
console.log(message3);

